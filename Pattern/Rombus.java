public class Rombus{
public static void main(String arg[]){
    int a=5;
    for(int i=0;i<a;i++){
        for(int j=i;j<=a;j++){
            System.out.print(" ");
        }
        for(int k=0;k<=a;k++){
            System.out.print("*");
        }
        System.out.print("\n");
    }
    //System.out.println();
}
}